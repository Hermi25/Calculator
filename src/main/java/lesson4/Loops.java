package lesson4;

public class Loops {
    public static void main(String[] args) {
        int n = 5;

        //loop for
        for (int i = 1; i <= n; i++) {
            System.out.println("Java is fun");
        }

        //loop while
        int i = 1;

        while (i <= n) {
            System.out.println("Java is not fun");
            i++;
        }


    }
}
